
public class Vehicle {
	private int Id;
	private int Year;
	private String Make;
	private String Model;
	
	//Id-------------
	public int getId() {
		return Id;
	}
	public void setId(int Id) {
		this.Id = Id;
	}
	
	//Year------------
	public int getYear() {
		return Year;
	}
	public void setYear(int Year) {
		this.Year = Year;
	}
	
	//Make------------
	public String getMake() {
		return Make;
	}
	public void setMake(String Make) {
		this.Make = Make;
	}
	
	//Model-------------
	public String getModel() {
		return Model;
	}
	public void setModel(String Model) {
		this.Model = Model;
	}
	
	
}
